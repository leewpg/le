        const newBalance = (simpleAuth.currentUser.balance || 0) + amount;
        
        // Update user balance in database
        const { data: updatedUser, error: updateError } = await supabase
            .from('profiles')
            .update({ balance: newBalance })
            .eq('id', simpleAuth.currentUser.id)
            .select()
            .single();

